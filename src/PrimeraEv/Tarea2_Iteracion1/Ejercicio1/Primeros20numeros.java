package PrimeraEv.Tarea2_Iteracion1.Ejercicio1;

public class Primeros20numeros {

    public static void main(String[] args) {
        int contador=1;
        int numfinal=20;
        while (contador <= numfinal) {
            System.out.println(contador);
            contador = contador +1;
        }
    }
}
